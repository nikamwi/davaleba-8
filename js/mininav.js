function showmininav(){
    document.getElementById("navigacia").style.display = "block"
    document.getElementById("x").style.display = "block"
    document.getElementById("mininav").style.display = "none"
}

function closenav(){
    document.getElementById("navigacia").style.display = "none"
    document.getElementById("x").style.display = "none"
    document.getElementById("mininav").style.display = "block"
}